import numpy as np

marks = np.array([78, 65, 90, 82, 65, 95, 78])

print("Student Marks:")
print(marks)

print()

print("Total Marks:")
print(np.sum(marks))

print()

print("Average Marks:")
print(np.mean(marks))

print()

print("Highest Marks:")
print(np.max(marks))

print()

print("Lowest Marks:")
print(np.min(marks))

print()

print("Median:")
print(np.median(marks))

print()

print("Standard Deviation:")
print(np.std(marks))

print()

print("Sorted Marks:")
print(np.sort(marks))

print()

print("Unique Marks:")
print(np.unique(marks))